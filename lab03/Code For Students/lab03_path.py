def graph_has_a_path(graph, start, end):
    visitados = [False]*len(graph)
    falta = [start]
    while(len(falta)>0 and not visitados[end]):
        nodo = falta.pop()
        visitados[nodo] = True
        for i in range(0, len(graph[nodo])):
            if graph[nodo][i] and not visitados[i]:
                falta.append(i)
    return visitados[end]
  
